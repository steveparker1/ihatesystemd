<h3>Why So Aggressive?</h3>
<div class="col-lg-10 col-lg-offset-1 text-justify">
<p>I don't think this website is particularly aggressive. It's just relaxed. Most IT documentation, whether written for
public consumption, such as official documentation, textbooks, even blogs, tend to assume that the reader and the author
both believe that the tool being discussed is at least generally beneficial and a positive, useful thing to be working with.
<br/>
I'm really not certain that that is the case with systemd. It has some terrible flaws, both technically and in the way in
which the developers interact with the community. It does not strike me as helpful to ignore that elephant in the room,
and to timidly try to write up, as best I can, how to get the best out of systemd, without also acknowledging these flaws.
It does the reader a disservice, and makes a liar of the author. It strikes me that a more lighthearted,
honest, maybe even playful and at times disrespectful approach works better for me, and therefore maybe for others. If the
Emperor truly has no clothes, then somebody has to be prepared to stand up and say so!</p>
<p>True, the "i hate systemd" moniker could be construed as, well, hateful. But genuinely, it is the most common reaction
I have observed, whether face-to-face or online, to this highly disruptive software which (for better or worse), it is no
longer possible to ignore.</p>
